package com.zavtech.morpheus.index;

/**
 * A consumer interface to walk through all entries in an index
 *
 * @param <K>   the key type for index
 *
 * <p>This is open source software released under the <a href="http://www.apache.org/licenses/LICENSE-2.0">Apache 2.0 License</a></p>
 *
 * @author  Xavier Witdouck
 */
public interface IndexConsumer<K> {

    /**
     * Called for each entry in an Index when calling forEachEntry
     * @param key       the key reference
     * @param index     the index value for key
     */
    void accept(K key, int index);
}
