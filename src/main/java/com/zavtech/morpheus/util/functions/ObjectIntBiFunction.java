package com.zavtech.morpheus.util.functions;

/**
 * A functional interface that operates on some object and a corresponding index value
 *
 * @param <I>   the input argument type
 * @param <O>   the output result type
 *
 * <p>This is open source software released under the <a href="http://www.apache.org/licenses/LICENSE-2.0">Apache 2.0 License</a></p>
 *
 * @author  Xavier Witdouck
 */
public interface ObjectIntBiFunction<I,O> {

    /**
     *
     * @param value
     * @param index
     * @return
     */
    O apply(I value, int index);
}
